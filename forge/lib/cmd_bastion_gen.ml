  -- ROUTE: GET /api/%s
  (:get,    Cons("api", Cons("%s", Nil))) ->
    %s.Handlers.%sHandler.index(conn)

  -- ROUTE: GET /api/%s/:id
  (:get,    Cons("api", Cons("%s", Cons(id, Nil)))) ->
    %s.Handlers.%sHandler.show(conn, id)

