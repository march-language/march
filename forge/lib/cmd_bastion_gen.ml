(* ------------------------------------------------------------------ run_json *)

let run_json context_name module_name field_specs =
  let fields_result = List.map parse_field field_specs in
  let errors =
    List.filter_map
      (function Error e -> Some e | Ok _ -> None)
      fields_result
  in
  if errors <> [] then Error (String.concat "\n" errors)
  else
    let fields =
      List.filter_map
        (function Ok f -> Some f | Error _ -> None)
        fields_result
    in
    let app_name    = detect_app_name () in
    let lower_app   = String.lowercase_ascii app_name in
    let lower_mod   = String.lowercase_ascii module_name in
    let lower_ctx   = String.lowercase_ascii context_name in
    let table_name  = lower_mod ^ "s" in

    (* 1 — Schema *)
    let schema_dir = Filename.concat "src" (Filename.concat "app" "schemas") in
    mkdir_p schema_dir;
    let schema_file = Filename.concat schema_dir (lower_mod ^ ".march") in
    write_file schema_file
      (json_schema_source ~app_name ~module_name ~table_name ~fields);
    Printf.printf "* creating %s\n%!" schema_file;

    (* 2 — Migration *)
    let migration_dir =
      Filename.concat "priv" (Filename.concat "depot" "migrations")
    in
    mkdir_p migration_dir;
    let timestamp = int_of_float (Unix.time ()) in
    let migration_file =
      Filename.concat migration_dir
        (Printf.sprintf "%d_create_%s.march" timestamp table_name)
    in
    write_file migration_file
      (migration_source ~module_name ~table_name ~fields);
    Printf.printf "* creating %s\n%!" migration_file;

    (* 3 — Context *)
    let context_dir = Filename.concat "src" (Filename.concat "app" "contexts") in
    mkdir_p context_dir;
    let context_file = Filename.concat context_dir (lower_ctx ^ ".march") in
    write_file context_file
      (context_source ~app_name ~context_name ~module_name);
    Printf.printf "* creating %s\n%!" context_file;

    (* 4 — Handler *)
    let handler_dir = Filename.concat "src" (Filename.concat "app" "handlers") in
    mkdir_p handler_dir;
    let handler_file =
      Filename.concat handler_dir (lower_mod ^ "_handler.march")
    in
    write_file handler_file
      (handler_source ~app_name ~context_name ~module_name);
    Printf.printf "* creating %s\n%!" handler_file;

    (* 5 — Test *)
    let test_dir = Filename.concat "test" "handlers" in
    mkdir_p test_dir;
    let test_file =
      Filename.concat test_dir (lower_mod ^ "_handler_test.march")
    in
    write_file test_file
      (handler_test_source ~app_name ~module_name);
    Printf.printf "* creating %s\n%!" test_file;

    (* 6 — Print route snippet *)
    let _ = lower_app in
    print_route_snippet ~app_name ~module_name ~lower_ctx;

    Ok ()

(* ------------------------------------------------------------------ run_schema *)

let run_schema module_name table_name field_specs =
  let fields_result = List.map parse_field field_specs in
  let errors =
    List.filter_map
      (function Error e -> Some e | Ok _ -> None)
      fields_result
  in
  if errors <> [] then Error (String.concat "\n" errors)
  else
    let fields =
      List.filter_map
        (function Ok f -> Some f | Error _ -> None)
        fields_result
    in
    let app_name = detect_app_name () in

    (* Generate schema file *)
    let schema_dir = Filename.concat "lib" (String.lowercase_ascii app_name) in
    mkdir_p schema_dir;
    let schema_file =
      Filename.concat schema_dir
        (String.lowercase_ascii module_name ^ ".march")
    in
    let schema_content =
      schema_source ~app_name ~module_name ~table_name ~fields
    in
    write_file schema_file schema_content;
    Printf.printf "* creating %s\n%!" schema_file;

    (* Generate migration file *)
    let migration_dir =
      Filename.concat "priv" (Filename.concat "depot" "migrations")
    in
    mkdir_p migration_dir;
    let timestamp = int_of_float (Unix.time ()) in
    let migration_file =
      Filename.concat migration_dir
        (Printf.sprintf "%d_create_%s.march" timestamp table_name)
    in
    let migration_content =
      migration_source ~module_name ~table_name ~fields
    in
    write_file migration_file migration_content;
    Printf.printf "* creating %s\n%!" migration_file;

    Ok ()
