(** cmd_bastion_gen.ml — forge bastion gen schema <Module> <table> field:type ...

    Generate a Depot.Schema module and migration file from CLI arguments. *)

let snake_to_pascal s =
  let parts = String.split_on_char '_' s in
  parts
  |> List.map (fun p ->
       if String.length p = 0 then ""
       else String.capitalize_ascii p)
  |> String.concat ""

let march_type_of = function
  | "string" -> "String"
  | "integer" | "int" -> "Int"
  | "float" -> "Float"
  | "bool" | "boolean" -> "Bool"
  | "uuid" -> "UUID"
  | "datetime" -> "UtcDatetime"
  | "date" -> "Date"
  | "time" -> "Time"
  | "map" | "json" -> "Map"
  | "binary" -> "Binary"
  | other -> other

let parse_field spec =
  match String.split_on_char ':' spec with
  | [name; typ] -> Ok (name, typ)
  | _ -> Error (Printf.sprintf "invalid field spec: %s (expected name:type)" spec)

let schema_source ~app_name ~module_name ~table_name ~fields =
  let field_defs =
    fields
    |> List.map (fun (name, typ) ->
         Printf.sprintf "      %s = \"%s\"" name (march_type_of typ))
    |> String.concat ",\n"
  in
  let field_names =
    fields
    |> List.map (fun (name, _) -> Printf.sprintf "\"%s\"" name)
    |> String.concat ", "
  in
  Printf.sprintf
    {|-- Auto-generated by forge bastion gen schema

mod %s.%s do

fn schema() do
  Depot.Schema.define("%s", {
    fields = {
%s,
      inserted_at = "UtcDatetime",
      updated_at = "UtcDatetime"
    }
  })
end

fn gate(params) do
  Depot.Gate.cast_record(Depot.Schema.blank(schema()), params, [%s])
    |> Depot.Gate.validate_required([%s])
end

fn update_gate(base, params) do
  Depot.Gate.cast_record(base, params, [%s])
end

end
|}
    app_name module_name table_name field_defs field_names field_names field_names

let migration_source ~module_name ~table_name ~fields =
  let col_defs =
    fields
    |> List.map (fun (name, typ) ->
         Printf.sprintf "    %s = (\"%s\", { null = false })" name
           (march_type_of typ))
    |> String.concat ",\n"
  in
  let timestamp = int_of_float (Unix.time ()) in
  let migration_mod = Printf.sprintf "Create%s" module_name in
  Printf.sprintf
    {|-- Auto-generated by forge bastion gen schema

mod Migrations.%s do

fn up() do
  Depot.Migration.create_table("%s", {
    id = ("UUID", { primary_key = true }),
%s,
    inserted_at = ("UtcDatetime", { null = false }),
    updated_at = ("UtcDatetime", { null = false })
  })
end

fn down() do
  Depot.Migration.drop_table("%s")
end

fn version() do
  %d
end

end
|}
    migration_mod table_name col_defs table_name timestamp

let mkdir_p dir =
  let parts = String.split_on_char '/' dir in
  let _ =
    List.fold_left
      (fun acc p ->
        let path = if acc = "" then p else acc ^ "/" ^ p in
        (if path <> "" && not (Sys.file_exists path) then
           Unix.mkdir path 0o755);
        path)
      "" parts
  in
  ()

let write_file path content =
  let oc = open_out path in
  output_string oc content;
  close_out oc

let detect_app_name () =
  let name = Filename.basename (Sys.getcwd ()) in
  snake_to_pascal name

(* ------------------------------------------------------------------ simple substitution helper *)

(** Replace all occurrences of [from] with [to_] in [s]. *)
let str_replace_all ~from ~to_ s =
  let from_len = String.length from in
  if from_len = 0 then s
  else begin
    let buf = Buffer.create (String.length s) in
    let i   = ref 0 in
    let n   = String.length s in
    while !i < n do
      if !i + from_len <= n && String.sub s !i from_len = from then begin
        Buffer.add_string buf to_;
        i := !i + from_len
      end else begin
        Buffer.add_char buf s.[!i];
        incr i
      end
    done;
    Buffer.contents buf
  end

(** Apply a list of [(placeholder, value)] substitutions to [tmpl].
    Placeholders are written as {{KEY}} in the template. *)
let render vars tmpl =
  List.fold_left
    (fun s (k, v) -> str_replace_all ~from:("{{" ^ k ^ "}}") ~to_:v s)
    tmpl vars

(* ------------------------------------------------------------------ json gen templates *)

let json_schema_source ~app_name ~module_name ~table_name ~fields =
  let field_defs =
    fields
    |> List.map (fun (name, typ) ->
         Printf.sprintf "      %s = \"%s\"" name (march_type_of typ))
    |> String.concat ",\n"
  in
  let field_names =
    fields
    |> List.map (fun (name, _) -> Printf.sprintf "\"%s\"" name)
    |> String.concat ", "
  in
  render [
    ("APP",         app_name);
    ("MODULE",      module_name);
    ("TABLE",       table_name);
    ("FIELD_DEFS",  field_defs);
    ("FIELD_NAMES", field_names);
  ] {|-- Auto-generated by forge bastion gen json

mod {{APP}}.{{MODULE}} do

fn schema() do
  Depot.Schema.define("{{TABLE}}", {
    fields = {
{{FIELD_DEFS}},
      inserted_at = "UtcDatetime",
      updated_at = "UtcDatetime"
    }
  })
end

fn gate(params) do
  Depot.Gate.cast_record(Depot.Schema.blank(schema()), params, [{{FIELD_NAMES}}])
    |> Depot.Gate.validate_required([{{FIELD_NAMES}}])
end

fn update_gate(base, params) do
  Depot.Gate.cast_record(base, params, [{{FIELD_NAMES}}])
end

end
|}

let context_source ~app_name ~context_name ~module_name =
  let lower_mod = String.lowercase_ascii module_name in
  render [
    ("APP",      app_name);
    ("CTX",      context_name);
    ("MOD",      module_name);
    ("lmod",     lower_mod);
  ] {|-- Auto-generated by forge bastion gen json

mod {{APP}}.{{CTX}} do

fn list_{{lmod}}s() do
  {{APP}}.Repo.all({{APP}}.{{MOD}}.schema())
end

fn get_{{lmod}}(id) do
  {{APP}}.Repo.get({{APP}}.{{MOD}}.schema(), id)
end

fn get_{{lmod}}!(id) do
  {{APP}}.Repo.get!({{APP}}.{{MOD}}.schema(), id)
end

fn create_{{lmod}}(params) do
  let gate = {{APP}}.{{MOD}}.gate(params)
  match Depot.Gate.is_valid(gate) do
    true  -> {{APP}}.Repo.insert(gate)
    false -> Err(gate)
  end
end

fn update_{{lmod}}({{lmod}}, params) do
  let gate = {{APP}}.{{MOD}}.update_gate({{lmod}}, params)
  match Depot.Gate.is_valid(gate) do
    true  -> {{APP}}.Repo.update(gate)
    false -> Err(gate)
  end
end

fn delete_{{lmod}}({{lmod}}) do
  {{APP}}.Repo.delete({{lmod}})
end

end
|}

let handler_source ~app_name ~context_name ~module_name =
  let lower_mod = String.lowercase_ascii module_name in
  let lower_ctx = String.lowercase_ascii context_name in
  render [
    ("APP",  app_name);
    ("CTX",  context_name);
    ("MOD",  module_name);
    ("lmod", lower_mod);
    ("lctx", lower_ctx);
  ] {|-- Auto-generated by forge bastion gen json

mod {{APP}}.Handlers.{{MOD}}Handler do

alias Bastion.Conn as Conn

-- GET /api/{{lctx}}s
fn index(conn) do
  let items = {{APP}}.{{CTX}}.list_{{lmod}}s()
  Conn.json(conn, 200, items)
end

-- GET /api/{{lctx}}s/:id
fn show(conn, id) do
  match {{APP}}.{{CTX}}.get_{{lmod}}(id) do
    Some(item) ->
      Conn.json(conn, 200, item)
    None ->
      Conn.json(conn, 404, { error = "not found" })
  end
end

-- POST /api/{{lctx}}s
fn create(conn) do
  let params = Conn.json_params(conn)
  match {{APP}}.{{CTX}}.create_{{lmod}}(params) do
    Ok(item) ->
      Conn.json(conn, 201, item)
    Err(gate) ->
      Conn.json(conn, 422, { errors = Depot.Gate.errors(gate) })
  end
end

-- PUT /api/{{lctx}}s/:id
fn update(conn, id) do
  match {{APP}}.{{CTX}}.get_{{lmod}}(id) do
    None ->
      Conn.json(conn, 404, { error = "not found" })
    Some(item) ->
      let params = Conn.json_params(conn)
      match {{APP}}.{{CTX}}.update_{{lmod}}(item, params) do
        Ok(updated) ->
          Conn.json(conn, 200, updated)
        Err(gate) ->
          Conn.json(conn, 422, { errors = Depot.Gate.errors(gate) })
      end
  end
end

-- DELETE /api/{{lctx}}s/:id
fn delete(conn, id) do
  match {{APP}}.{{CTX}}.get_{{lmod}}(id) do
    None ->
      Conn.json(conn, 404, { error = "not found" })
    Some(item) ->
      {{APP}}.{{CTX}}.delete_{{lmod}}(item)
      Conn.json(conn, 204, {})
  end
end

-- Fallback: 404 JSON response for unmatched routes
fn not_found(conn) do
  Conn.json(conn, 404, { error = "not found" })
end

end
|}

