-- ---------------------------------------------------------------------------
-- Email confirmation
-- ---------------------------------------------------------------------------

fn deliver_user_confirmation_instructions(user, url_fn) do
  let (token, gate) = UserToken.build_email_token(user, "confirm")
  match Repo.insert(UserToken.schema(), gate) do
  Ok(_) ->
    UserNotifier.deliver_confirmation_instructions(user, url_fn(token))
    :ok
  Err(_) -> :error
  end
end

fn confirm_user(token_str) do
  let hashed = Crypto.sha256(token_str)
  match Repo.get_by(UserToken.schema(), { token = hashed, context = "confirm" }) do
  None -> Err("invalid or expired confirmation token")
  Some(tok) ->
    match Repo.get(User.schema(), tok.user_id) do
    None -> Err("user not found")
    Some(user) ->
      let now = UtcDatetime.now()
      let gate = Gate.cast_record(user, { confirmed_at = now }, ["confirmed_at"])
      match Repo.update(User.schema(), gate) do
      Ok(confirmed_user) ->
        Repo.delete(UserToken.schema(), tok.id)
        Ok(confirmed_user)
      Err(g) -> Err(g)
      end
    end
  end
