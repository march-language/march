  let tokens = Q.from(UserToken.schema())
    |> Q.where_(fn tok -> tok.user_id == user.id && tok.context == context)
    |> Q.execute(Repo)
  List.each(tokens, fn tok -> Repo.delete(UserToken.schema(), tok.id))
end

end
