  let hashed = Crypto.sha256(token_str)
  match Repo.get_by(UserToken.schema(), { token = hashed, context = "reset_password" }) do
  None -> None
  Some(tok) -> Repo.get(User.schema(), tok.user_id)
  end
end

fn reset_user_password(user, params) do
  let gate = User.update_password_gate(user, params)
  if Gate.is_valid(gate) do
    let final_gate = User.hash_password(gate)
    match Repo.update(User.schema(), final_gate) do
    Ok(updated_user) ->
      delete_all_user_tokens(user, "reset_password")
      Ok(updated_user)
    Err(g) -> Err(g)
    end
  else Err(gate) end
end

