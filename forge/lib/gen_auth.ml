-- Registration
-- ---------------------------------------------------------------------------

-- Register a new user.  Returns Ok(user) or Err(gate) with validation errors.
fn register_user(params) do
  let gate = User.registration_gate(params)
  if Gate.is_valid(gate) do
    let final_gate = User.hash_password(gate)
    match Repo.insert(User.schema(), final_gate) do
    Ok(user) ->
      UserNotifier.deliver_welcome(user)
      Ok(user)
    Err(g) -> Err(g)
    end
  else Err(gate) end
end

