let user_schema_source pascal =
  Printf.sprintf {|-- Auto-generated by forge bastion gen auth
-- Represents a registered user account.
-- Passwords are hashed with PBKDF2-SHA256 via Crypto.hash_password.

mod %s.Accounts.User do

alias Depot.Gate as Gate
alias Depot.Schema as Schema

fn schema() do
  Schema.define("users", {
    fields = {
      email = "String",
      hashed_password = "String",
      confirmed_at = ("UtcDatetime", { null = true }),
      inserted_at = "UtcDatetime",
      updated_at = "UtcDatetime"
    }
  })
end

-- ---------------------------------------------------------------------------
-- Gates
-- ---------------------------------------------------------------------------

-- Gate for new user registration.
-- Validates email format, uniqueness, and password strength.
-- Does NOT hash the password — call hash_password/1 after validation.
fn registration_gate(params) do
  Gate.cast(params, ["email", "password"])
    |> Gate.validate_required(["email", "password"])
    |> validate_email()
    |> validate_password()
end

-- Gate for updating a user's email address.
fn update_email_gate(user, params) do
  Gate.cast_record(user, params, ["email"])
    |> Gate.validate_required(["email"])
    |> validate_email()
end

-- Gate for updating a user's password.
fn update_password_gate(user, params) do
  Gate.cast_record(user, params, ["password", "password_confirmation"])
    |> Gate.validate_required(["password", "password_confirmation"])
    |> validate_password()
    |> Gate.validate_confirmation("password")
end

-- ---------------------------------------------------------------------------
-- Password helpers
-- ---------------------------------------------------------------------------

-- Hash the plain-text "password" change in a gate and replace it with
-- "hashed_password".  Returns the gate unchanged if no password change exists.
fn hash_password(gate) do
  match Gate.get_change(gate, "password") do
  None -> gate
  Some(password) ->
    let hashed = Crypto.hash_password(password)
    Gate.put_change(gate, "hashed_password", hashed)
      |> Gate.delete_change("password")
  end
end

-- Return true if `password` matches the stored hash on `user`.
fn valid_password(user, password) do
  Crypto.verify_password(password, user.hashed_password)
end

-- ---------------------------------------------------------------------------
-- Private validators
-- ---------------------------------------------------------------------------

pfn validate_email(gate) do
  Gate.validate_format(gate, "email", ".+@.+\\..+")
    |> Gate.validate_length("email", [LenMax(160)])
    |> Gate.unique_constraint("email",
        [ConstraintName("users_email_index"),
         ConstraintMessage("has already been taken")])
end

pfn validate_password(gate) do
  Gate.validate_length(gate, "password", [LenMin(12), LenMax(72)])
end

end
|} pascal

