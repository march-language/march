-- ---------------------------------------------------------------------------

fn update_user_email(user, params) do
  let gate = User.update_email_gate(user, params)
  if Gate.is_valid(gate) do
    Repo.update(User.schema(), gate)
  else Err(gate) end
end

fn update_user_password(user, params) do
  let gate = User.update_password_gate(user, params)
  if Gate.is_valid(gate) do
    let final_gate = User.hash_password(gate)
    match Repo.update(User.schema(), final_gate) do
    Ok(updated_user) ->
      delete_all_user_tokens(user, "session")
      Ok(updated_user)
    Err(g) -> Err(g)
    end
  else Err(gate) end
end

-- Build a gate for the settings form (no persistence, just for rendering).
fn change_user_password(user) do
  User.update_password_gate(user, [])
end

-- ---------------------------------------------------------------------------
-- Session tokens
-- ---------------------------------------------------------------------------

fn create_session_token(user) do
  let (token, gate) = UserToken.build_session_token(user)
  match Repo.insert(UserToken.schema(), gate) do
  Ok(_) -> Ok(token)
  Err(g) -> Err(g)
  end
