-- Build a session token gate.
-- Returns (raw_token, gate) — insert the gate, hand the raw token to the browser.
fn build_session_token(user) do
  let token = Crypto.random_hex(32)
  let gate = Gate.cast(
    [("user_id", user.id), ("token", token), ("context", "session")],
    ["user_id", "token", "context"]
  )
  (token, gate)
end

-- Build an email token gate (confirmation, password reset, email change).
-- Returns (raw_token, gate) — insert the gate, email the raw token.
-- The stored token is the SHA-256 hash of the raw token.
fn build_email_token(user, context) do
  let token = Crypto.random_hex(32)
  let hashed = Crypto.sha256(token)
  let gate = Gate.cast(
    [("user_id", user.id), ("token", hashed),
     ("context", context), ("sent_to", user.email)],
    ["user_id", "token", "context", "sent_to"]
  )
  (token, gate)
end

end
